mensagem = 'EU adoro cachorros'
mensagem2 = '    EU adoro cachorros'

print()
#normal
print(mensagem)

#upper case
print(mensagem.upper())

#lower case
print(mensagem.lower())

#primeira maiuscula
print(mensagem.capitalize())

#achar letra (diferencia palavras, letras maiusculas e minusculas)
print(mensagem.find('c'))

#replace
print(mensagem.replace('EU', 'Henrique').replace('adoro', 'adora'))

#strip
print()
print(mensagem2)
print(mensagem2.strip())
